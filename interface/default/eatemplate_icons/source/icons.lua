-- Returns the icon ID based upon the passed in careerID(which is based upon the line numbers from careernames_[m/f].txt
-- (sigh) Don't confuse the careerlines with careernames.
function Icons.GetCareerIconIDFromCareerNamesID(careerNamesID) 

-- Note: As the character reaches a new tier, their careerID changes, so we have to check every career at every tier.
-- Note: The careerID is the same regardless of gender.
-- The career Icons are the same regardless of tier.
-- The carrer Icons are the same regardless of gender.

	if careerNamesID ~= nil and careerNamesID >= Icons.careers.MIN and careerNamesID <= Icons.careers.MAX then
		return Icons.careers[careerNamesID]
	else
		return 0
	end

end

function Icons.GetCareerIconIDFromCareerLine(careerLine)
    if careerLine
    then
		return Icons.careerLines[careerLine]
	end
end 
